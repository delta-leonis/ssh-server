/**
 * The Enum Priority.
 *
 * @author Rimon Oz
 */
public enum Priority {

    /** The lowest priority. */
    LOWEST,

    /** The lower priority. */
    LOWER,

    /** The low priority. */
    LOW,

    /** The medium priority. */
    MEDIUM,

    /** The high priority. */
    HIGH,

    /** The higher priority. */
    HIGHER,

    /** The highest priority. */
    HIGHEST,

    /**
     * The most urgent priority trumping all priorities that have ever been
     * prioritized.
     */
    URGENT
}
