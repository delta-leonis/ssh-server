package field3d.core.gameobjects;

import field3d.core.game.Game;
import field3d.core.gameobjects.input.KeyInputHandler;

public abstract class GameObject {
	
	
	///////////////////////////////////////////////////////////////////////////////////////////////
	//
	//	Private methods
	//
	///////////////////////////////////////////////////////////////////////////////////////////////
	private Game _game;
	

	
	///////////////////////////////////////////////////////////////////////////////////////////////
	//
	//	Constructors
	//
	///////////////////////////////////////////////////////////////////////////////////////////////
	public GameObject(Game game) {
		
		// Setting game
		_game = game;
	}

	
	///////////////////////////////////////////////////////////////////////////////////////////////
	//
	//	Abstract methods
	//
	///////////////////////////////////////////////////////////////////////////////////////////////
	public abstract void Initialize();
	public abstract void Update(long timeDivNano);
	public abstract void Destroy();
	
	
	///////////////////////////////////////////////////////////////////////////////////////////////
	//
	//	Getters
	//
	///////////////////////////////////////////////////////////////////////////////////////////////
	public Game GetGame() { return _game; }
	public KeyInputHandler GetKeyInputHandler() { return _game.GetKeyInputHandler(); }
}
